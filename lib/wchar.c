wchar_t *wmemset(wchar_t *wcs, wchar_t wc, size_t n)
{
	assert(wcs!=0);

	asm("cld\n" "rep\n" "stosw" : : "c" (n), "a" (wc), "D" (wcs));
	return wcs;
}
