void ohci_init()
{
	void *pci;
	pci = pci_get_type(NULL, 0xC, 0x3, 0x10);
	while (pci != 0)
	{
		kprintf(2, "found USB (OHCI) host controller\n", 0);
		pci = pci_get_type(pci, 0xC, 0x3, 0x10);
	}
}
